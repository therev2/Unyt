class Event {
  final int id;
  final String title;
  final String description;
  final String date;
  final String location;
  final String image;
  final String organizer;

  Event({
    required this.id,
    required this.title,
    required this.description,
    required this.date,
    required this.location,
    required this.image,
    required this.organizer,
  });
}
